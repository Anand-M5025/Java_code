package com.anand;
public class passing_value_example {

    public static void main(String[] args) {
        String name = " Anand Maurya ";
        greet(name);
    }

    static void greet(String naam) {
        System.out.println(naam);
    }
}
    // here naam doesn't care what particular
    //  string value is . Like name is passed which value is Anand Maurya
    // basically name is replaced by naam value





